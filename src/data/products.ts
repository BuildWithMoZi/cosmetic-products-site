export function getCategories(): CategoryInfo[] {
  return categoryMeta.map((cat) => {
    const categoryProducts = products.filter((p) => p.category === cat.slug);
    return {
      ...cat,
      productCount: categoryProducts.length,
      coverImage: categoryProducts[0]?.image,
    };
  });
}

/** @deprecated Use getCategories() for dynamic counts */
export const categories = getCategories();

export function getProductsByCategory(category: string): Product[] {
  return products.filter((p) => p.category === category);
}

export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.tags.includes("bestseller"));
}

export function getNewProducts(): Product[] {
  return products.filter((p) => p.tags.includes("new"));
}

export function getSaleProducts(): Product[] {
  return products.filter((p) => p.tags.includes("sale") || p.originalPrice);
}

export function getAllProducts(): Product[] {
  return products;
}
